    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if(allStockJList.getSelectedValuesList().size() == 0){
            return;
        }
        
        ArrayList<String> userSelectedStocks = (ArrayList <String>)allStockJList.getSelectedValuesList();
        for (String stock : userSelectedStocks) {
            userSelectedJlistModel.addElement(stock);
        }
        
        userSelectedStockJList.setModel(userSelectedJlistModel);
    }//GEN-LAST:event_addButtonActionPerformed

    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        if(userSelectedStockJList.getSelectedValuesList().size() == 0){
            return;
        }
        
        ArrayList<String> userSelectedStocks = (ArrayList<String>) userSelectedStockJList.getSelectedValuesList();
        for (String stock : userSelectedStocks) {
            userSelectedJlistModel.removeElement(stock);
        }
    }//GEN-LAST:event_removeButtonActionPerformed

    private void createPortfolioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createPortfolioButtonActionPerformed
        if(userSelectedStockJList.getModel().getSize() == 0){
            return;
        }
        
        Portfolio portfolio = new Portfolio(avaiableStock);
        for (int i = 0; i < userSelectedStockJList.getModel().getSize(); i++) {
            String stock = userSelectedStockJList.getModel().getElementAt(i).toString().split(",")[0];
            portfolio.Add(stock);
        }
        
        setVisible(false);
        new StockMonitoringSystemMainPanel(portfolio).setVisible(true);
    }//GEN-LAST:event_createPortfolioButtonActionPerformed

