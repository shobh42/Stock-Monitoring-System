    public void showStocks(){
        Map<String, Stock> allStocks = avaiableStock.getAvailableStock();
        DefaultListModel<String> allStockJlistModel = new DefaultListModel<String>();
        for(String symbol: allStocks.keySet()){
            allStockJlistModel.addElement(symbol + ", " + allStocks.get(symbol).getCompanyName());
        }
        
        allStockJList.setModel(allStockJlistModel);
    }

